public class MultiplesOf3 {
    public static void main(String[] args) {
        // defining variable to store the starting number
        int start_number= 3;
        // do-while loop to print multiples
        do{
          System.out.print(start_number);
          System.out.print(' ');
          // incrementing number by 3
          start_number +=3;
        }while(start_number <=36); // stopping condition
    
    }
}
